package com.raj.oauth.oauth;

public class User {
    private String logiin;
    private String password;

    public String getLogiin() {
        return logiin;
    }

    public void setLogiin(String logiin) {
        this.logiin = logiin;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
